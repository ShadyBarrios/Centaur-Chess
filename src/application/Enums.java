package application;

/**
 * Contains multiple enums for organization purposes
 * @author Shady
 *
 */
public interface Enums{
	/**
	 * Contains the two different colors for players.
	 * @author Shady
	 *
	 */
	enum Players {WHITE, BLACK}
	/**
	 * Contains each type of chess piece.
	 * @author Shady
	 */
	enum Pieces {PAWN, ROOK, KNIGHT, BISHOP, KING, QUEEN}
	
	/**
	 * Contains the two general thicknesses for chess pieces
	 * @author Shady
	 */
	enum Thickness {SKINNY, THICK}
}